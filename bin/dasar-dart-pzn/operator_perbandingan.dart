/* operator menghasilkan 2 buah nilai yang hasilnya adalah bool atau boolean
bisa digunakan di tipe data number dan string

== (sama dengan)
!= (tidak sama dengan)
> (lebih dari)
< (kurang dari)
>= (lebih dari atau sama dengan)
<= (kecil dari atau sama dengan)

*/
void main() {
  var first = 10;
  var last = 10;

  print(first == last);
  print(first != last);
  print(first > last);
  print(first < last);
  print(first >= last);
  print(first <= last);
}
