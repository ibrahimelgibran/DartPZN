/*
+ = pertambahkan
-, -expression = pengurangan atau negatif (-expression bisa pakai angka)
* = perkalian
/ = pembagian, hasil double
~/ = pembagian, atau jika ingin hasilnya hasil intejer 
% = sisa bagi
 */

void main() {
  var first = 10;
  var last = 3;

  print(first + last);
  print(first - last);
  print(first * last);
  print(first / last);
  print(first ~/ last);
  print(first % last);
}
